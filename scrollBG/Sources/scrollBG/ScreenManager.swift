        let MODE0: UInt16 = 0x00
        let BG0_ENABLE: UInt16 = 0x100
        display_control.pointee = MODE0 | BG0_ENABLE
        setup_background()
    }
    
    // 背景0をセットアップする関数
    func setup_background() {
        // パレットをパレットメモリにロード
        for i in 0..<PALETTE_SIZE {
            bg_palette_ptr[i] = background_palette[i]
        }
        // タイルデータをキャラクターブロック0にロード
        // background_data は [UInt8] なので、[UInt16] に変換
        let dest = char_block(0)
        for i in 0..<background_data.count / 2 {
            let low = UInt16(background_data[i * 2])
            let high = UInt16(background_data[i * 2 + 1]) << 8
            dest[i] = low | high
        }
        // 各ビットフィールドを個別に計算
        let priority: UInt16 = 0
        let charBlock: UInt16 = 0 << 2
        let mosaicFlag: UInt16 = 0 << 6
        let colorMode: UInt16 = 1 << 7
        let screenBlock: UInt16 = 16 << 8
        let wrappingFlag: UInt16 = 1 << 13 // 画像の繰り返し
        let bgSize: UInt16 = 0 << 14 // 背景サイズ、0が256x256
        // すべてを組み合わせる
        let bg0ControlValue = priority | charBlock | mosaicFlag | colorMode | screenBlock | wrappingFlag | bgSize
        // レジスタに代入
        bg0_control.pointee = bg0ControlValue
        // スクリーンブロック0を使用
        let screenDest = screen_block(0)
        for i in 0..<map.count {
            screenDest[i] = map[i]
        }
    }
    
    // 4つのキャラクターブロック（0-3）のいずれかへのポインタを返す関数
    func char_block(_ block: UInt32) -> UnsafeMutablePointer<UInt16> {
        return getPointer(at: 0x6000000 + (block * 0x4000))
    }
    
    // 32のスクリーンブロック（0-31）のいずれかへのポインタを返す関数
    func screen_block(_ block: UInt32) -> UnsafeMutablePointer<UInt16> {
        return getPointer(at: 0x6008000 + (block * 0x800))
    }
    
    func run() {
        var count: UInt32 = 0
        var xscroll: UInt16 = 0
        var yscroll: UInt16 = 0

        while true {
            wait_vblank()
            count += 1
            if count > 2000 {
                // 矢印キーの入力に応じてスクロール位置を更新
                if button_pressed(BUTTON_DOWN) == 1 {
                    yscroll += 1
                }
                if button_pressed(BUTTON_UP) == 1 {
                    yscroll -= 1
                }
                if button_pressed(BUTTON_RIGHT) == 1 {
                    xscroll += 1
                }
                if button_pressed(BUTTON_LEFT) == 1 {
                    xscroll -= 1
                }
                count = 0
            }
            // スクロール位置をスクロールレジスタに反映
            bg0_x_scroll.pointee = xscroll
            bg0_y_scroll.pointee = yscroll
        }
    }
    // VBlank待機
    func wait_vblank() {
        while scanline_counter.pointee >= 160 {}
        while scanline_counter.pointee < 160 {}
    }
    
    // 特定のボタンが押されたかをチェックする関数
    func button_pressed(_ button: UInt16) -> UInt8 {
        let pressed = buttons.pointee & button
        return pressed == 0 ? 1 : 0
    }
}

// メモリマップに基づくメモリアクセスのヘルパー関数
func getPointer<T>(at address: UInt32) -> UnsafeMutablePointer<T> {
    return UnsafeMutablePointer<T>(bitPattern: Int(address))!
}

